package lesson_03.classwork1103;

public class main01 {
    public static void main(String[] args) {
        // Обьявляем целочисленную переменную и записываем в нее число 10
        int number1 = 10;
        // Обьявляем вещественную переменную и записываем в нее число 5.25
        // Академически, это называется "число с плавающей точкой", "Floating point numder"
        double number2 = 5.25;
        // Обьявляем символьную переменную и записываем символ а
        char letter = 'a';
        // Шаблон sout поможет быстро написать System.out.println!
        System.out.println(number1);
        System.out.println(number2);
        System.out.println(letter);


    }
}
